class SDKUpgradeWorker(UpgradeWorkerBase):
    '''
    Upgrade tool for SDK of OpenRTK
    '''

    def __init__(self, uart, file_content):
        super(SDKUpgradeWorker, self).__init__()
        self._uart = uart
